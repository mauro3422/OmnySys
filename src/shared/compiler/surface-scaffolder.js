/**
 * @fileoverview surface-scaffolder.js
 * Auto-generates canonical surface files from obligation metadata.
 *
 * Pattern learned from 7 hand-crafted surfaces:
 *   1. JSDoc header with source table + control plane field
 *   2. loadXxx(db, options) — reads data, returns structured summary
 *   3. getXxxControlPlaneStatus(db) — returns status for control plane
 *   4. Default export with both functions
 *
 * @module shared/compiler/surface-scaffolder
 */

/**
 * Generates the complete content of a canonical surface file.
 * @param {object} obligation - Surface obligation from propagator
 * @returns {string} Complete JS file content
 */
export function generateSurfaceFileContent(obligation) {
  const { sourceTable, controlPlaneField, rowCount, description } = obligation;
  const surfaceName = obligation.id?.replace('surface_obligation:', '') || extractSurfaceName(sourceTable);
  const fnName = capitalize(surfaceName);
  const loadFn = `load${fnName}`;
  const statusFn = `get${fnName}ControlPlaneStatus`;

  // Determine table type for specialized stats
  const tableType = classifyTable(sourceTable);

  return `/**
 * @fileoverview ${toKebabCase(surfaceName)}.js
 * Canonical surface for ${description || sourceTable}.
 *
 * Source of truth: ${sourceTable} table (${rowCount}+ rows)
 * Control plane field: ${controlPlaneField || surfaceName}
 *
 * AUTO-GENERATED by surface-scaffolder.js — review and customize as needed.
 */

/**
 * Loads ${surfaceName} summary from canonical data.
 * @param {object} db - SQLite database connection
 * @param {object} [options] - Query options
 * @param {number} [options.lastN=100] - Number of recent records
 * @returns {object} ${surfaceName} summary
 */
export function ${loadFn}(db, options = {}) {
  const { lastN = 100 } = options;

  // Overall stats
  const stats = db.prepare(\`
    SELECT
      COUNT(*) as total_records,
      MIN(created_at) as first_record,
      MAX(created_at) as last_record
      ${tableType === 'timestamped' ? ', AVG(CASE WHEN updated_at IS NOT NULL THEN updated_at END) as avg_updated_at' : ''}
      ${tableType === 'latency' ? ', AVG(delivery_latency_ms) as avg_latency_ms' : ''}
      ${tableType === 'latency' ? ', MAX(delivery_latency_ms) as max_latency_ms' : ''}
    FROM ${sourceTable}
  \`).get();

  // Recent records
  const recentRecords = db.prepare(\`
    SELECT *
    FROM ${sourceTable}
    ORDER BY id DESC
    LIMIT ?
  \`).all(lastN);

  // By category/type (generic grouping by common columns)
  const byCategory = db.prepare(\`
    SELECT
      COALESCE(event_type, component, tool_name, client_id, society_type, 'other') as category,
      COUNT(*) as count
    FROM ${sourceTable}
    GROUP BY category
    ORDER BY count DESC
    LIMIT 20
  \`).all();

  // Top entities (generic)
  const topEntities = db.prepare(\`
    SELECT
      COALESCE(atom_id, tool_name, client_id, society_id, source_path, 'N/A') as entity,
      COUNT(*) as record_count
    FROM ${sourceTable}
    GROUP BY entity
    ORDER BY record_count DESC
    LIMIT 20
  \`).all();

  return {
    state: 'canonical',
    sourceTable: '${sourceTable}',
    rowCount: stats?.total_records || 0,
    stats: stats || {},
    recentRecords: (recentRecords || []).reverse(),
    byCategory: byCategory || [],
    topEntities: topEntities || [],
    summary: build${fnName}Summary(stats, byCategory)
  };
}

function build${fnName}Summary(stats, byCategory) {
  if (!stats || stats.total_records === 0) return '${toKebabCase(surfaceName)}=none';
  const catSummary = (byCategory || []).map(c => \`\${c.category}=\${c.count}\`).join(', ');
  return \`${toKebabCase(surfaceName)}=\${stats.total_records}records | \${catSummary || 'no categories'}\`;
}

/**
 * Gets ${controlPlaneField || surfaceName} control plane status.
 * @param {object} db - SQLite database connection
 * @returns {object} Control plane ${controlPlaneField || surfaceName} status
 */
export function ${statusFn}(db) {
  const data = ${loadFn}(db, { lastN: 50 });

  if (data.rowCount === 0) {
    return {
      state: 'missing',
      reason: 'No ${sourceTable} records found',
      recommendation: 'Records are generated during Layer B analysis or MCP runtime.'
    };
  }

  return {
    state: data.rowCount > 100 ? 'ready' : 'watching',
    totalRecords: data.rowCount,
    lastRecordAt: data.stats?.last_record || data.stats?.last_event || null,
    topCategory: data.byCategory?.[0]?.category || null,
    summary: data.summary
  };
}

export default { ${loadFn}, ${statusFn} };
`;
}

/**
 * Extracts a surface name from a table name.
 * e.g., 'mcp_topology_events' → 'topology'
 */
function extractSurfaceName(tableName) {
  // Remove common prefixes/suffixes
  return tableName
    .replace(/^mcp_/, '')
    .replace(/_events$/, '')
    .replace(/_runs$/, '')
    .replace(/_sessions$/, '')
    .replace(/_dependencies$/, '')
    .replace(/_connections$/, '')
    .replace(/_assessments$/, '')
    .replace(/_issues$/, '');
}

/**
 * Classifies a table to generate specialized stats.
 */
function classifyTable(tableName) {
  if (tableName.includes('topology') || tableName.includes('session')) return 'basic';
  if (tableName.includes('delivery') || tableName.includes('latency')) return 'latency';
  if (tableName.includes('event') || tableName.includes('runs')) return 'timestamped';
  if (tableName.includes('societies') || tableName.includes('dependencies')) return 'categorical';
  return 'basic';
}

function capitalize(str) {
  if (!str) return '';
  return str.charAt(0).toUpperCase() + str.slice(1).replace(/_([a-z])/g, (_, c) => c.toUpperCase());
}

function toKebabCase(str) {
  return str.replace(/([A-Z])/g, '-$1').toLowerCase().replace(/^-/, '');
}

export default { generateSurfaceFileContent };
